package arrays;

public class Two_dimensional_array {

public static void main(String[ ] args) {
    int ar1[ ][ ] = {{101,202,303},{404,505,606},{707,808,909}};
    
    for (int i = 0; i < ar1.length; i++){
        for (int j = 0; j < ar1[i].length; j++){
            System.out.print("ar1["+i+"]["+j+"] = "+ar1[i][j] + " ");
}
System.out.println();
}
}
}

 
