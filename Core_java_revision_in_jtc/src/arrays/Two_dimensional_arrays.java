package arrays;

public class Two_dimensional_arrays {

public static void main(String[] args) {
    int ar1[][] = new int[3][3];
    
    for (int i = 0; i < ar1.length; i++){
        for (int j = 0; j < ar1[i].length; j++){
            System.out.print("ar1["+i+"]["+j+"] = "+ar1[i][j] + " ");
}
System.out.println();
}

}
}
         
