package control.statement;

public class If_else_Statement {

public static void main(String[] args) {
    int number1 = 20;
    int number2 = 15;

    if (number1 < number2) {
        System.out.println("number1 is less than number2");
    } else {
        System.out.println("number1 is greater than number2");
    }
   }
}            

