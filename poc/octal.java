class O{
	public static void main(String arg[]){
		int a=012345670;
		System.out.println(a);//2739128
		System.out.printf("%o", a);//12345670
		System.out.println();
	
	System.out.printf("%d", a);
		System.out.println();
		int b=0x123af;
		System.out.println(b);//74671
		
		System.out.printf("%X", b);
		System.out.println();
		int c=0B1010101;
		System.out.println(c);
	}
}
		